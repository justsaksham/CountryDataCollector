package com.example.myapplication.model;

public class DataHolder {
    public String name, capital, flag, region,
    subregion, population, borders , languages ;
    public DataHolder(String name,String capital, String flag, String region,
                      String   subregion, String population, String borders , String languages){
        this.name=name;
        this.capital=capital;
        this.flag=flag;
        this.region=region;
        this.subregion=subregion;
        this.population=population;
        this.borders=borders;
        this.languages=languages;

    }
}
